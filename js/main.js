const savedLanguage = storage.get("s2guidehub-lang", "en");

const state = {
  lang: content[savedLanguage] ? savedLanguage : "en",
  filter: "all",
  query: "",
};

const $ = (selector) => document.querySelector(selector);
const $$ = (selector) => Array.from(document.querySelectorAll(selector));

function setLanguage(lang) {
  if (!content[lang]) {
    lang = "en";
  }

  state.lang = lang;
  storage.set("s2guidehub-lang", lang);
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";

  $$("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (content[lang][key]) {
      node.textContent = content[lang][key];
    }
  });

  $$("[data-i18n-placeholder]").forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    if (content[lang][key]) {
      node.setAttribute("placeholder", content[lang][key]);
    }
  });

  $$(".lang-button").forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  renderFeaturedUnlocks();
  renderUnlocks();
}

function getFilteredUnlocks() {
  const lang = state.lang;
  const query = state.query.trim().toLowerCase();

  return unlocks.filter((item) => {
    const localized = item[lang];
    const matchesFilter = state.filter === "all" || item.category === state.filter;
    const haystack = [
      item.id,
      item.category,
      item.stage,
      item.risk,
      item.routeDifficulty,
      item.status,
      item.distance,
      item.confidence,
      localized.name,
      localized.valuePoint,
      localized.summary,
      localized.value,
      ...item.tags,
    ]
      .join(" ")
      .toLowerCase();

    return matchesFilter && (!query || haystack.includes(query));
  });
}

function createChip(text, variant = "") {
  const chip = document.createElement("span");
  chip.className = variant ? `chip ${variant}` : "chip";
  chip.textContent = text;
  return chip;
}

function createLinkChip(href, text) {
  const link = document.createElement("a");
  link.className = "chip chip-link";
  link.href = href;
  link.textContent = text;
  return link;
}

function createUnlockCard(item, options = {}) {
  const lang = state.lang;
  const localized = item[lang];
  const card = document.createElement("article");
  card.className = options.featured ? "unlock-card featured-unlock-card" : "unlock-card";

  const header = document.createElement("div");
  header.className = "unlock-card-header";

  const titleWrap = document.createElement("div");
  const category = document.createElement("p");
  category.className = "unlock-category";
  category.textContent = categoryLabels[lang][item.category] || item.category;

  const title = document.createElement("h3");
  title.textContent = localized.name;
  titleWrap.append(category, title);

  const score = document.createElement("div");
  score.className = "score-badge";
  score.setAttribute("aria-label", `${content[lang].scoreLabel} ${item.score}`);
  score.innerHTML = `<strong>${item.score.toFixed(1)}</strong><span>${content[lang].scoreLabel}</span>`;

  header.append(titleWrap, score);

  const summary = document.createElement("p");
  summary.className = "unlock-summary";
  summary.textContent = localized.summary;

  const value = document.createElement("div");
  value.className = "unlock-value-block";
  const valueLabel = document.createElement("span");
  valueLabel.textContent = content[lang].valuePointLabel || "Value point";
  const valueCopy = document.createElement("p");
  valueCopy.textContent = localized.valuePoint || localized.value;
  value.append(valueLabel, valueCopy);

  const meta = document.createElement("dl");
  meta.className = "unlock-meta";
  meta.append(
    createMetaItem(content[lang].stageLabel, stageLabels[lang][item.stage] || item.stage),
    createMetaItem(
      content[lang].routeDifficultyLabel || "Route difficulty",
      routeDifficultyLabels[lang][item.routeDifficulty] || item.routeDifficulty,
    ),
    createMetaItem(content[lang].riskLabel, riskLabels[lang][item.risk] || item.risk),
    createMetaItem(
      content[lang].statusLabel || "Status",
      statusLabels[lang][item.status] || item.status,
    ),
  );

  const chips = document.createElement("div");
  chips.className = "result-meta";
  chips.append(
    createChip(content[lang].needsGameplayCheck, "needs-check"),
    createChip(content[lang].noCoordinates, "no-coordinates"),
    createChip(content[lang].routeDraft, "framework"),
  );

  if (options.featured) {
    chips.append(
      item.pageUrl
        ? createLinkChip(item.pageUrl, content[lang].openGuide || "Open guide")
        : createChip(content[lang].guideQueued || "Guide queued", "warning"),
    );
  }

  card.append(header, summary, value, meta, chips);
  return card;
}

function createMetaItem(label, value) {
  const wrap = document.createElement("div");
  const dt = document.createElement("dt");
  const dd = document.createElement("dd");
  dt.textContent = label;
  dd.textContent = value;
  wrap.append(dt, dd);
  return wrap;
}

function renderFeaturedUnlocks() {
  const target = $("#featured-unlocks");
  if (!target) return;

  target.innerHTML = "";
  unlocks.forEach((item) => {
    target.append(createUnlockCard(item, { featured: true }));
  });
}

function renderUnlocks() {
  const results = $("#unlock-results");
  if (!results) return;

  const lang = state.lang;
  const filtered = getFilteredUnlocks();
  results.innerHTML = "";

  if (!filtered.length) {
    const empty = document.createElement("p");
    empty.className = "empty-state";
    empty.textContent = content[lang].emptyState;
    results.append(empty);
    return;
  }

  filtered.forEach((item) => {
    results.append(createUnlockCard(item));
  });
}

function bindEvents() {
  $$(".lang-button").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.lang));
  });

  $$(".filter-button").forEach((button) => {
    button.addEventListener("click", () => {
      state.filter = button.dataset.filter;
      $$(".filter-button").forEach((item) => {
        item.classList.toggle("is-active", item === button);
      });
      renderUnlocks();
    });
  });

  const search = $("#unlock-search");
  if (search) {
    search.addEventListener("input", (event) => {
      state.query = event.target.value;
      renderUnlocks();
    });
  }
}

bindEvents();
setLanguage(state.lang);
